package org.hao.po;

public class TripleValue {

	private Object valueA;
	private Object valueB;
	private Object valueC;
	public Object getValueA() {
		return valueA;
	}
	public void setValueA(Object valueA) {
		this.valueA = valueA;
	}
	public Object getValueB() {
		return valueB;
	}
	public void setValueB(Object valueB) {
		this.valueB = valueB;
	}
	public Object getValueC() {
		return valueC;
	}
	public void setValueC(Object valueC) {
		this.valueC = valueC;
	}
	
	
}
