package org.hao.po;

import java.util.List;

public class Lottery {

	private int lotteryid;
	private String lotteryData;
	private List lotteryDataList;
	private String lotteryDate;
	public int getLotteryid() {
		return lotteryid;
	}
	public void setLotteryid(int lotteryid) {
		this.lotteryid = lotteryid;
	}
	public String getLotteryData() {
		return lotteryData;
	}
	public void setLotteryData(String lotteryData) {
		this.lotteryData = lotteryData;
	}
	public List getLotteryDataList() {
		return lotteryDataList;
	}
	public void setLotteryDataList(List lotteryDataList) {
		this.lotteryDataList = lotteryDataList;
	}
	public String getLotteryDate() {
		return lotteryDate;
	}
	public void setLotteryDate(String lotteryDate) {
		this.lotteryDate = lotteryDate;
	}
	
	
	
}
